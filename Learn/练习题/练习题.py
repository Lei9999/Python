"""
从控制台输入一个三位数，如果是水仙花数就打印“是水仙花数”
否则打印“不是水仙花数”
# 153 = 1^3 + 5^3 + 3^3
"""
num0 = int(input("请输入一个三位数："))
a = num0 // 100
b = (num0 - (a * 100)) // 10
c = (num0 - (a * 100) - (b * 10))
print(a, b, c)
if num0 == (a ** 3) + (b ** 3) + (c ** 3):
    print("是水仙花数")
else:
    print("不是水仙花数")
